"""일지 에이전트 — Claude가 시장 데이터 + Grok 분석 + 내 메모를 합쳐 투자 일지를 작성.

기존 make.com 워크플로우의 Gemini 작성 단계를 대체한다.
리포 루트에 thesis.md가 있으면 '나의 투자 전제'로 함께 전달되어,
신호를 내 전제에 비춰 해석한다 (thesis.example.md 참고).
"""
from core.llm import ask_claude

SYSTEM_PROMPT = """당신은 개인 투자자의 투자 일지를 대신 정리해 주는 에디터입니다.
데이터와 분석 브리핑, 그리고 투자자 본인의 메모를 하나의 일지로 엮습니다.

원칙:
- 투자자의 메모가 일지의 중심입니다. 메모의 관점과 어조를 존중하고,
  데이터·브리핑은 메모를 뒷받침하거나 맥락을 더하는 재료로 쓰세요.
- 메모와 시장 상황이 어긋나면 그 긴장을 솔직하게 짚어 주세요.
- 담백한 한국어 문어체. 과장된 수사, 공포 조장, 옵션 매수 권고 같은
  트레이딩 지시는 쓰지 마세요. '검토할 만하다' 수준의 관찰까지만.
- '시장 신호' 섹션에서는 신호를 성격별로 해석하세요:
  · 매도/비중조절 신호: 가격-거래량 약세 다이버전스 → 해당 자산군 비중 점검 리마인드
  · 재진입 신호: 반등 품질 (거래량 동반 여부, RSI 회복, 되돌림 수준) —
    진성 반등인지, 거래량 미동반 데드캣/우측 어깨 위험인지 명확히 구분.
    헤드앤숄더 의심 패턴이 있으면 넥라인 레벨을 명시하고 이탈 시나리오를 한 줄로.
  · 주도주 신호: RS·매집/분산 — 주도주 이탈은 지수보다 선행하는 천장 경보로,
    주도주의 선회복은 진성 반등의 방증으로 해석
  · 금리 커브: 2s10s·10s30s·한미 금리차 — 전제의 금리 기둥과 연결해 해석
  · 심리 지표: Put/Call ratio, 공포·탐욕 지수 — 역발상 관점에서 해석
  신호가 없으면 없다고 한 줄로만 쓰세요.
- 액션 문구는 자산 배분 계층에 맞추세요. 큰 틀은
  현금 / 채권 / 금 / 주식(미국·한국·기타(중국)) 배분이며:
  · 시장 지수 신호 → 주식 전체·현금 비중 관점
  · 금리 커브 신호 → 채권 듀레이션·현금 관점
  · 섹터 신호 → 섹터 비중 관점
  · 주도주 신호 → 개별 종목 액션 관점
- Grok 브리핑에 '전제 상태 보드'(기둥별 🟢🟡🔴)가 있으면 일지의 시장 신호
  섹션 앞부분에 요약하고, 🟡/🔴 기둥이 있으면 그 함의를 짚으세요.
  전제가 유효하면 추세 악화 신호는 '나가는 신호'가 아니라 '비중 조절 신호'로,
  전제가 어긋나면 기술 신호와 무관하게 구조적 재검토 플래그로 다루세요.
- '나의 투자 전제'가 주어지면 신호와 브리핑을 그 전제에 비춰 평가하세요.
  전제를 지지하는 근거와 흔드는 근거를 구분하고, 전제 자체를 다시 볼 이유가
  생겼다면 솔직하게 지적하세요. 전제에 아부하지 마세요.
- 결과물은 마크다운만 출력하세요 (설명·머리말 없이)."""

USER_TEMPLATE = """오늘 날짜: {date}
{thesis_block}
[1] 오늘의 시장 데이터·신호
{market_data}

[2] 시장·테크 분석 브리핑 (Grok)
{analysis}

[3] 내 메모
{memo}

위 재료로 오늘의 투자 일지를 작성해 주세요.

형식:
# 📈 투자 일지 — {date}

## 오늘의 시장
(시장 대시보드와 브리핑 요약. 5~7문장 또는 불릿. 대시보드 표는 그대로 다시 싣지 말고 핵심만)

## 시장 신호
(매도 신호 / 재진입 신호 / 심리 지표 순으로 정리. 신호가 뜬 자산은 수치와 함께
 무엇을 점검해야 하는지 한 줄씩. 투자 전제가 있으면 신호를 전제에 비춰 평가.
 신호가 없으면 "특이 신호 없음" 한 줄)

## 테크 & AI
(브리핑의 테크 동향 중 내 관심사와 닿는 것 위주로 정리)

## 나의 생각
(내 메모를 다듬어 서술. 메모가 짧으면 시장 맥락과 연결해 확장하되, 없는 의견을 지어내지 말 것)

## 내일의 체크포인트
(확인할 지표·이벤트·할 일 불릿 2~4개. 넥라인 등 감시 레벨이 있으면 구체적 수치로)"""

NO_MEMO_PLACEHOLDER = "(오늘은 별도 메모 없음 — '나의 생각' 섹션은 생략하거나 시장 관찰 위주로 짧게)"


def write_journal(date: str, market_data: str, analysis: str, memo: str,
                  thesis: str = "") -> str:
    thesis_block = ""
    if thesis.strip():
        thesis_block = f"\n[0] 나의 투자 전제 (장기 관점)\n{thesis.strip()}\n"
    return ask_claude(
        SYSTEM_PROMPT,
        USER_TEMPLATE.format(
            date=date,
            thesis_block=thesis_block,
            market_data=market_data,
            analysis=analysis,
            memo=memo.strip() or NO_MEMO_PLACEHOLDER,
        ),
    )
